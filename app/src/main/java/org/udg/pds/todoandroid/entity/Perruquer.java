package org.udg.pds.todoandroid.entity;

public class Perruquer {
  public long id;
  public String nomPerruquer;
  public String nomUsuari;


  public Perruquer() { }

  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }

  public String getNomPerruquer() {
    return nomPerruquer;
  }

  public String getNomUsuari() {
    return nomUsuari;
  }

}
